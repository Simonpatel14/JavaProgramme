package corejava;

public class VariablesPrimitiveDataTypes2 {

    public static void main(String[] args) {
     /*byte
     min: -128
     max: 127
     Default: 0
    */
     byte byt = 100;
     System.out.println("Byte Value: " + byt);
     /*
     short
     min: -32,768
     max: 32,767
     Default: 0
     */
     short sho=1000;
     System.out.println("Short Value:  "  +sho);
     /*
     int
     min: -2,147,483,648
     min:  2,147,483,647
     Default: 0
     */
     int in  =10000;
     System.out.println("Int Value:  "  + in);
     /*
     long
     min: -2^63
     max: 2^63 -1
     Default: 0
     */
     long lon = 100000;
     System.out.println("Long value:  "  + lon);
     /*
     float
     Default: 0.0f
     */
     float flo = 10.5f;
     System.out.println("Float value: "   + flo);
     /*
     Double
     Default:0.0d
     */
     double dou = 60.33;
     System.out.println("Double value: "   + dou);
     /*
     boolean
     false & true
     Default: false
     */
     boolean boo = true;
     System.out.println("Boolean value: "   + boo);
     /*
     char
     min: 0
     max: 65,535
     */
     char cha ='s';
     System.out.println("Char Value:   "  + cha );


    }
}